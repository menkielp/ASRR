namespace ASRR.Core
{
    /// <summary>
    /// Animacje
    /// </summary>
    public enum Animation
    {

        /// <summary>
        /// Brak animacji
        /// </summary>
        None = 0,

        /// <summary>
        /// Animacja wslizgiwania sie z lewej strony
        /// </summary>
        SladeInFromLeft = 1,

        /// <summary>
        /// Animacja wyslizgiwania sie do lewej strony
        /// </summary>
        SladeOutToLeft = 2,

        /// <summary>
        /// Animacja wslizgiwania sie z lewej strony
        /// </summary>
        SladeInFromRight = 3,

        /// <summary>
        /// Animacja wyslizgiwania sie do lewej strony
        /// </summary>
        SladeOutToRight = 4,

        /// <summary>
        /// Animacja pojawania sie
        /// </summary>
        FadeIn = 5,

        /// <summary>
        /// Animacja znikania
        /// </summary>
        FadeOut = 6,

        /// <summary>
        /// Animacja pojawiania sie do okreslonej przezroczystosci
        /// </summary>
        LightFadeIn = 7,

    }
}
